public class Pessoa {

        // Atributos da classe
        private String nome;
        private String cpf;

        // Metodos.



    // Pega ou recebe os valores do atributo.
    public String getCpf() {
        return cpf;
    }

    // Definindo o valor do atributo.
    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
}
